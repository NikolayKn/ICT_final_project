function A_mod = Bit_Swapping (A,L,b,c,sb);
for i = 1:b
    
    j=0;
    % Freezing a vulnerable bit:
    j = j+1;
    for k = 1:sb
        if ( (W(2,j) == Bsw(k)) && (Bw(W(2,j)) > Bz(W(2,j)))))
            A(W(1,j) = [];
            break
        end
    end
    
    
    % Freezing a vulnerable bit:
    j=0;
    j = j+1;
    for k = 1:sb
        if ( (W(2,j) == Bsw(k)) && (Bw(W(2,j)) > Bz(W(2,j)))))
            A(W(1,j) = [];
            break
        end
    end
end
end